package Matma;

public class Matma {

    public static int abs(int x) {
        if (x >= 0) {
            return x;
        } else {
            return -x;
        }
    }

    public static float abs(float x) {
        if (x >= 0) {
            return x;
        } else {
            return -x;
        }
    }
}
